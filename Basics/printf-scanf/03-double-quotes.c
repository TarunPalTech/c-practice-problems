// Problem: Write a program to print "India" on the screen. (Remember to print in double quotes)

// Description: 
// In C, double quotes are used to define string literals. To print actual double quotes in output, we use the escape sequence \".
//  The backslash (\) is an escape character in C. So, \" tells the compiler to treat the double quote as a character, not the end of the string.

#include<stdio.h>

int main(){
    printf("\"India\"");
    return 0;
}